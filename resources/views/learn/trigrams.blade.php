
<p>Os trigramas são símbolos formados por três linhas que compõem o I Ching. Eles representam as oito possibilidades de combinação de Yin Yang em três linhas, e são elementos que estruturam o livro chinês I Ching. Cada um dos oito trigramas é composto por três linhas, que podem ser inteiras (yang) ou partidas (yin). É a partir dos atributos dos trigramas que se deduzem o sentido e os diferentes significados de cada um dos 64 hexagramas. Os trigramas também são estudados em seu conjunto e não apenas para compor o conteúdo dos hexagramas. Os mestres chineses são hábeis em traduzir os ciclos coletivos e individuais, bem como deduzir a conduta oportuna a partir das diferentes distribuições circulares dos oito trigramas.</p>

